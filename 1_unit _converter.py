#converting from centimeters to meters and centimeters to kilo meters
s=float(input("please enter the value centimeter:"))
meters=s/100
kilometers=s/1000;
print("%.2f Centimeters = %.2f Meters" %(s,meters))
print("%.2f Centimeters = %.2f Kilometers" %(s,kilometers))
#converting kilograms to grams
grams=float(input("please enter in grams:"))
kilograms=grams/1000;
print("%.2f grams= %.2f kilograms" %(grams,kilograms))